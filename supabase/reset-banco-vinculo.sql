create or replace function public.criar_perfil_novo_usuario()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  novo_psicologo_id uuid;
begin
  insert into public.perfis (id, nome, tipo_usuario)
  values (
    new.id,
    coalesce(nullif(new.raw_user_meta_data ->> 'nome',''), split_part(new.email, '@', 1), 'Usuário'),
    case when new.raw_user_meta_data ->> 'tipo_usuario' = 'psicologo' then 'psicologo' else 'cliente' end
  )
  on conflict (id) do update set
    nome = excluded.nome,
    tipo_usuario = excluded.tipo_usuario;

  if new.raw_user_meta_data ->> 'tipo_usuario' = 'psicologo' then
    insert into public.psicologos (
      perfil_id, crp, estado_crp, biografia, modalidade, valor_consulta, perfil_ativo
    ) values (
      new.id,
      coalesce(nullif(new.raw_user_meta_data ->> 'crp',''), 'CRP pendente'),
      coalesce(nullif(new.raw_user_meta_data ->> 'estado_crp',''), 'Não informado'),
      coalesce(new.raw_user_meta_data ->> 'biografia', ''),
      coalesce(new.raw_user_meta_data ->> 'modalidade', 'online'),
      coalesce((nullif(new.raw_user_meta_data ->> 'valor_consulta',''))::numeric, 0),
      true
    )
    on conflict (perfil_id) do update set
      crp = excluded.crp,
      estado_crp = excluded.estado_crp,
      biografia = excluded.biografia,
      modalidade = excluded.modalidade,
      valor_consulta = excluded.valor_consulta,
      perfil_ativo = true
    returning id into novo_psicologo_id;

    insert into public.psicologo_especialidades (psicologo_id, especialidade_id)
    select novo_psicologo_id, e.id
    from public.especialidades e
    where e.nome in (
      select jsonb_array_elements_text(coalesce(new.raw_user_meta_data -> 'especialidades', '[]'::jsonb))
    )
    on conflict do nothing;
  end if;

  return new;
end;
$$;

create trigger criar_perfil_apos_signup
after insert on auth.users
for each row execute procedure public.criar_perfil_novo_usuario();

create or replace function public.garantir_meu_perfil()
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  usuario auth.users%rowtype;
  novo_psicologo_id uuid;
begin
  select * into usuario from auth.users where id = auth.uid();
  if usuario.id is null then
    raise exception 'Usuário não autenticado.';
  end if;

  insert into public.perfis(id,nome,tipo_usuario)
  values (
    usuario.id,
    coalesce(nullif(usuario.raw_user_meta_data->>'nome',''), split_part(usuario.email,'@',1), 'Usuário'),
    case when usuario.raw_user_meta_data->>'tipo_usuario'='psicologo' then 'psicologo' else 'cliente' end
  )
  on conflict(id) do update set
    nome = coalesce(public.perfis.nome, excluded.nome),
    tipo_usuario = excluded.tipo_usuario;

  if usuario.raw_user_meta_data->>'tipo_usuario'='psicologo' then
    insert into public.psicologos(perfil_id,crp,estado_crp,biografia,modalidade,valor_consulta,perfil_ativo)
    values (
      usuario.id,
      coalesce(nullif(usuario.raw_user_meta_data->>'crp',''), 'CRP pendente'),
      coalesce(nullif(usuario.raw_user_meta_data->>'estado_crp',''), 'Não informado'),
      coalesce(usuario.raw_user_meta_data->>'biografia',''),
      coalesce(usuario.raw_user_meta_data->>'modalidade','online'),
      coalesce((nullif(usuario.raw_user_meta_data->>'valor_consulta',''))::numeric,0),
      true
    )
    on conflict(perfil_id) do update set perfil_ativo = true
    returning id into novo_psicologo_id;
  end if;
end;
$$;

grant execute on function public.garantir_meu_perfil() to authenticated;

create or replace function public.criar_agendamento(p_disponibilidade_id uuid, p_modalidade text default 'online')
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  v_cliente uuid := auth.uid();
  v_slot public.disponibilidades%rowtype;
  v_psicologo public.psicologos%rowtype;
  v_id uuid;
begin
  if v_cliente is null then raise exception 'Usuário não autenticado.'; end if;

  select * into v_slot
  from public.disponibilidades
  where id = p_disponibilidade_id
  for update;

  if not found or v_slot.status <> 'disponivel' then
    raise exception 'Horário indisponível.';
  end if;

  select * into v_psicologo
  from public.psicologos
  where id = v_slot.psicologo_id and perfil_ativo = true;

  if not found then raise exception 'Psicólogo indisponível.'; end if;

  insert into public.agendamentos(
    cliente_id, psicologo_id, disponibilidade_id, data_consulta,
    hora_inicio, hora_fim, modalidade, valor, status
  )
  values (
    v_cliente, v_psicologo.id, v_slot.id, v_slot.data,
    v_slot.hora_inicio, v_slot.hora_fim,
    case when p_modalidade = 'presencial' then 'presencial' else 'online' end,
    v_psicologo.valor_consulta, 'pendente'
  )
  returning id into v_id;

  update public.disponibilidades set status='reservado' where id=v_slot.id;
  return v_id;
end;
$$;

grant execute on function public.criar_agendamento(uuid,text) to authenticated;

create or replace function public.sincronizar_disponibilidade_agendamento()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if new.disponibilidade_id is not null and new.status='cancelado' and old.status <> 'cancelado' then
    update public.disponibilidades set status='disponivel' where id=new.disponibilidade_id;
  end if;
  return new;
end;
$$;

create trigger sincronizar_disponibilidade_apos_agendamento
after update of status on public.agendamentos
for each row execute procedure public.sincronizar_disponibilidade_agendamento();

