create table public.perfis (
  id uuid primary key references auth.users(id) on delete cascade,
  nome text not null,
  tipo_usuario text not null default 'cliente'
    check (tipo_usuario in ('cliente', 'psicologo')),
  foto_url text,
  telefone text,
  data_nascimento date,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.psicologos (
  id uuid primary key default gen_random_uuid(),
  perfil_id uuid not null unique references public.perfis(id) on delete cascade,
  crp text not null,
  estado_crp text not null,
  biografia text,
  modalidade text not null default 'online'
    check (modalidade in ('online', 'presencial', 'ambos')),
  valor_consulta numeric(10,2) not null default 0
    check (valor_consulta >= 0),
  anos_experiencia integer not null default 0
    check (anos_experiencia >= 0),
  perfil_ativo boolean not null default true,
  verificado boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.especialidades (
  id bigint generated by default as identity primary key,
  nome text not null unique,
  created_at timestamptz not null default now()
);

create table public.psicologo_especialidades (
  psicologo_id uuid not null references public.psicologos(id) on delete cascade,
  especialidade_id bigint not null references public.especialidades(id) on delete cascade,
  primary key (psicologo_id, especialidade_id)
);

create table public.triagens (
  id uuid primary key default gen_random_uuid(),
  cliente_id uuid not null references public.perfis(id) on delete cascade,
